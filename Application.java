package Edureka;
 
public class ArithmeticOperators {
      public static void main(String[] args) {
            int A = 10;
            int B = 20;
            System.out.println(A + B);
            System.out.println(A - B);
            System.out.println(A * B);
            System.out.println(A / B);
            System.out.println(A % B);
          }
 
}
